                    n = len(y)
                    if n < 3:
                        return None, None, None
                    xm, ym = x.mean(), y.mean()
                    Sxx = ((x - xm) ** 2).sum()
                    if Sxx <= 0:
                        return None, None, None
                    b1 = ((x - xm) * (y - ym)).sum() / Sxx
                    resid = y - (ym + b1 * (x - xm))
                    s2 = (resid ** 2).sum() / (n - 2)
                    if s2 <= 0:
                        return b1, None, None
                    t = b1 / math.sqrt(s2 / Sxx)
                    p = 2 * (1.0 - _norm_cdf(abs(t)))
                    return b1, t, p

